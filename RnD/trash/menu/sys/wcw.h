namespace Quick {

  template<typename T,T l,T h>
  struct Range {static constexpr bool chk(T c) {return l<=c&&c<=h;}};

  template<typename... OO> struct Ranges;

  template<> struct Ranges<> {template<typename T> static constexpr bool chk(T) {return false;}};

  template<typename O,typename... OO>
  struct Ranges<O,OO...> {
    using Base=O;
    using Next=Ranges<OO...>;
    template<typename T> static constexpr bool chk(T c) {return O::chk(c)||Next::chk(c);}
  };

};

